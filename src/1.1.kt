fun main(){
    var a:Int
    a=24
    print("Integer value: $a")
    var b:Char='P'
    print("\nCharacter value: $b")
    var c:Float=24.7f
    print("\nFloat value: $c")
    var d:Double=24.7219
    print("\nDouble value: $d")
    var e:String="MAD"
    print("\nString value: $e")
    var f:Boolean=true
    print("\nBoolean value: $f")
    var g:Short=-2
    print("\nShort value: $g")
    var h:Long=240721092212
    print("\nLong value: $h")
    var i:Byte=-124
    print("\nByte value: $i")
}
